function startCollectibleInt(state, pos){
    if(state == true && collectibleInterval == null){
        collectibleInterval = setInterval(function(){
        mp.game.audio.playSoundFromCoord(1, "ROUND_ENDING_STINGER_CUSTOM", pos.x, pos.y, pos.z, "CELEBRATION_SOUNDSET", false, 0, false);
        }, 4000);
    }
    else if(state == false && collectibleInterval != null){
        clearInterval(collectibleInterval);
        collectibleInterval = null;
    }
}

